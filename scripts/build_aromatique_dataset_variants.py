"""Build Aromatique KG variants for CR-HKGE baseline diagnostics.

The main `dataset-aromatique-kgat-ready` graph contains cross-reference
relations (`inspired_by`, `has_global_accord`, `belongs_to_global_family`) and
the computed product-product `sem_similar` relation. That is useful for
CR-HKGE, but it also lets a vanilla KGAT baseline consume part of the proposed
novelty. This script creates compact-relation variants for controlled
comparisons:

- dataset-aromatique-local-kgat-ready:
  local product attributes + sem_similar, no cross-reference relations.
- dataset-aromatique-attribute-kgat-ready:
  local product attributes only, no cross-reference and no sem_similar.
- dataset-aromatique-cr-hkge-nosim-ready:
  local product attributes + cross-reference relations, no sem_similar.

Entity IDs and train/test item IDs are preserved, so checkpoints can still be
evaluated against enriched/standard item subsets derived from the full dataset.
"""

from __future__ import annotations

import json
import shutil
from pathlib import Path


ROOT = Path(__file__).resolve().parents[1]
SOURCE = ROOT / "dataset-aromatique-kgat-ready"

VARIANTS = {
    "dataset-aromatique-local-kgat-ready": [
        "has_accord",
        "has_visual_note",
        "belongs_to_family",
        "sem_similar",
    ],
    "dataset-aromatique-attribute-kgat-ready": [
        "has_accord",
        "has_visual_note",
        "belongs_to_family",
    ],
    "dataset-aromatique-cr-hkge-nosim-ready": [
        "inspired_by",
        "has_accord",
        "has_visual_note",
        "belongs_to_family",
        "has_global_accord",
        "belongs_to_global_family",
    ],
}

COPY_FILES = [
    "entity2id.txt",
    "entity2id_typed.tsv",
    "old_to_new_entity_id.tsv",
    "product2id.tsv",
    "train.txt",
    "test.txt",
]


def read_relation_map(path: Path):
    rows = []
    for line in path.read_text(encoding="utf-8").splitlines()[1:]:
        if not line.strip():
            continue
        parts = line.split()
        rows.append((" ".join(parts[:-1]), int(parts[-1])))
    return rows


def write_relation_map(path: Path, relation_names):
    lines = [str(len(relation_names))]
    lines.extend("%s\t%d" % (name, idx) for idx, name in enumerate(relation_names))
    path.write_text("\n".join(lines) + "\n", encoding="utf-8")


def build_variant(target_name: str, keep_relation_names):
    target = ROOT / target_name
    if target.exists():
        shutil.rmtree(target)
    target.mkdir(parents=True)

    for filename in COPY_FILES:
        shutil.copy2(SOURCE / filename, target / filename)

    source_relations = read_relation_map(SOURCE / "relation2id.txt")
    name_to_old_id = {name: relation_id for name, relation_id in source_relations}
    old_to_new_id = {
        name_to_old_id[name]: new_id
        for new_id, name in enumerate(keep_relation_names)
    }

    kept_triples = []
    for line in (SOURCE / "kg_final.txt").read_text(encoding="utf-8").splitlines():
        if not line.strip():
            continue
        head, relation, tail = [int(part) for part in line.split()]
        if relation not in old_to_new_id:
            continue
        kept_triples.append((head, old_to_new_id[relation], tail))

    (target / "kg_final.txt").write_text(
        "\n".join("%d %d %d" % triple for triple in kept_triples) + "\n",
        encoding="utf-8",
    )
    write_relation_map(target / "relation2id.txt", keep_relation_names)

    summary = {
        "source_dataset": SOURCE.name,
        "variant_dataset": target_name,
        "kept_relations": keep_relation_names,
        "n_kg_triples": len(kept_triples),
        "relation_id_policy": "compact_remap_preserve_entity_ids",
        "train_test_item_ids": "preserved_from_source",
    }
    (target / "summary.json").write_text(json.dumps(summary, indent=2) + "\n", encoding="utf-8")

    readme = [
        "# %s" % target_name,
        "",
        "Generated by `scripts/build_aromatique_dataset_variants.py`.",
        "",
        "Kept relations:",
    ]
    readme.extend("- `%s`" % name for name in keep_relation_names)
    readme.extend([
        "",
        "Entity IDs and train/test item IDs are preserved from `dataset-aromatique-kgat-ready`.",
        "Relation IDs are compact-remapped to satisfy the original KGAT loader.",
        "",
    ])
    (target / "README.md").write_text("\n".join(readme), encoding="utf-8")

    print("%s: triples=%d, relations=%d" % (target_name, len(kept_triples), len(keep_relation_names)))


def main():
    for target_name, keep_relation_names in VARIANTS.items():
        build_variant(target_name, keep_relation_names)


if __name__ == "__main__":
    main()
